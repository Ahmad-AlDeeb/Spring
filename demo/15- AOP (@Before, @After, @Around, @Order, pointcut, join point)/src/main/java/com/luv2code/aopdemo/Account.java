package com.luv2code.aopdemo;

public class Account {

    /** Fields **/
    private String name;


    /** Constructors **/
    public Account() {

    }

    public Account(String name) {
        this.name = name;
    }


    /** Getters/Setters  **/
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
